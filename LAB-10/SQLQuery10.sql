--1.Combine information from student and result table using cross join or Cartesian product.
    SELECT * FROM STU_INFO CROSS JOIN RESULT
--2.Perform inner join on Student and Result tables.
    SELECT * FROM STU_INFO INNER JOIN RESULT ON STU_INFO.RNO = RESULT.RNO
--3.Perform the left outer join on Student and Result tables.
    SELECT * FROM STU_INFO LEFT OUTER JOIN RESULT ON STU_INFO.RNO = RESULT.RNO
--4.Perform the right outer join on Student and Result tables.
    SELECT * FROM STU_INFO RIGHT OUTER JOIN RESULT ON STU_INFO.RNO = RESULT.RNO
--5.Perform the full outer join on Student and Result tables.
    SELECT * FROM STU_INFO FULL OUTER JOIN RESULT ON STU_INFO.RNO = RESULT.RNO
--6.Display Rno, Name, Branch and SPI of all students.
    SELECT S.RNO,S.NAME,S.BRANCH,R.SPI FROM STU_INFO S , RESULT R WHERE S.RNO = R.RNO
--7.Display Rno, Name, Branch and SPI of CE branch’s student only.
    SELECT STU_INFO.RNO,STU_INFO.NAME,STU_INFO.BRANCH,RESULT.SPI FROM STU_INFO JOIN RESULT ON STU_INFO.RNO = RESULT.RNO WHERE STU_INFO.BRANCH = 'CE'
--8.Display Rno, Name, Branch and SPI of other than EC branch’s student only.
    SELECT STU_INFO.RNO,STU_INFO.NAME,STU_INFO.BRANCH,RESULT.SPI FROM STU_INFO JOIN RESULT ON STU_INFO.RNO = RESULT.RNO WHERE STU_INFO.BRANCH != 'EC'
--9.Display average result of each branch.
    SELECT BRANCH,AVG(SPI) FROM STU_INFO JOIN RESULT ON STU_INFO.RNO = RESULT.RNO GROUP BY BRANCH
--10.Display average result of CE and ME branch.
    SELECT BRANCH,AVG(SPI) FROM STU_INFO JOIN RESULT ON STU_INFO.RNO = RESULT.RNO WHERE BRANCH IN ('CE','ME') GROUP BY BRANCH
--11.Display Maximum and Minimum SPI of each branch.
     SELECT BRANCH,MAX(SPI),MIN(SPI) FROM STU_INFO JOIN RESULT ON STU_INFO.RNO = RESULT.RNO GROUP BY BRANCH
--12.Display branch wise student’s count in descending order.
     SELECT BRANCH,COUNT(BRANCH) FROM STU_INFO JOIN RESULT ON STU_INFO.RNO = RESULT.RNO GROUP BY BRANCH ORDER BY COUNT(BRANCH) DESC

