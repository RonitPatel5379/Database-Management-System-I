--1.Display average result of each branch and sort them in ascending order by SPI.
    SELECT BRANCH,AVG(SPI) FROM STU_INFO JOIN RESULT ON STU_INFO.RNO = RESULT.RNO GROUP BY BRANCH ORDER BY AVG(SPI)
--2.Display highest SPI from each branch and sort them in descending order.
    SELECT BRANCH,MAX(SPI) FROM STU_INFO JOIN RESULT ON STU_INFO.RNO = RESULT.RNO GROUP BY BRANCH ORDER BY MAX(SPI) DESC

